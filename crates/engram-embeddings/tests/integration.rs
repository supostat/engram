// ── Error propagation ──────────────────────────────────────────────────

#[test]
fn provider_error_propagates() {
    let provider = FailingEmbeddingProvider;
    let mut embedder = Embedder::new();

    let long_text =
        "a sufficiently long text that will not trigger hyde generation at all for testing";
    let result = embedder.embed_query(long_text, &provider, None);

    assert!(result.is_err());
    let error = result.unwrap_err();
    assert!(
        matches!(error, EmbeddingError::ProviderError(_)),
        "expected ProviderError, got: {error}"
    );
}

// ── Cache ──────────────────────────────────────────────────────────────

#[test]
fn cache_clear_works() {
    let provider = MockEmbeddingProvider { dimension: 4 };
    let mut embedder = Embedder::new();

    let query = "search for documents about rust programming and its unique memory safety features";

    embedder.embed_query(query, &provider, None).unwrap();
    assert_eq!(embedder.cache().len(), 1);

    embedder.clear_cache();
    assert!(embedder.cache().is_empty());

    // Re-embedding should work after cache clear
    embedder.embed_query(query, &provider, None).unwrap();
    assert_eq!(embedder.cache().len(), 1);
}

#[test]
fn cache_starts_empty() {
    let cache = EmbeddingCache::new();
    assert!(cache.is_empty());
    assert_eq!(cache.len(), 0);
}

// ── hyde module boundary tests ─────────────────────────────────────────

#[test]
fn should_use_hyde_boundary() {
    // Exactly 20 words -> false
    let twenty_words = "one two three four five six seven eight nine ten \
                        eleven twelve thirteen fourteen fifteen sixteen \
                        seventeen eighteen nineteen twenty";
    assert!(!hyde::should_use_hyde(twenty_words));
}

#[test]
fn should_use_hyde_under_threshold() {
    // 19 words -> true
    let nineteen_words = "one two three four five six seven eight nine ten \
                          eleven twelve thirteen fourteen fifteen sixteen \
                          seventeen eighteen nineteen";
    assert!(hyde::should_use_hyde(nineteen_words));
}

// ── EmbeddingError ─────────────────────────────────────────────────────

#[test]
fn embedding_error_display_format() {
    let provider_error =
        EmbeddingError::ProviderError(ApiError::EmbeddingApiUnavailable("timeout".into()));
    assert_eq!(
        provider_error.to_string(),
        "embedding provider error: [2001] embedding api unavailable: timeout"
    );
}

#[test]
fn embedding_error_from_api_error() {
    let api_error = ApiError::EmbeddingApiUnavailable("connection lost".into());
    let embedding_error: EmbeddingError = api_error.into();

    assert!(matches!(embedding_error, EmbeddingError::ProviderError(_)));
    assert_eq!(
        embedding_error.to_string(),
        "embedding provider error: [2001] embedding api unavailable: connection lost"
    );
}

