// ── Mock providers ─────────────────────────────────────────────────────

struct MockEmbeddingProvider {
    dimension: usize,
}

impl EmbeddingProvider for MockEmbeddingProvider {
    fn embed(&self, text: &str) -> Result<Vec<f32>, ApiError> {
        let mut embedding = vec![0.0_f32; self.dimension];
        for (index, byte) in text.bytes().enumerate() {
            embedding[index % self.dimension] += byte as f32 / 255.0;
        }
        Ok(embedding)
    }

    fn dimension(&self) -> usize {
        self.dimension
    }

    fn model_name(&self) -> &str {
        "mock-embedding"
    }
}

struct FailingEmbeddingProvider;

impl EmbeddingProvider for FailingEmbeddingProvider {
    fn embed(&self, _text: &str) -> Result<Vec<f32>, ApiError> {
        Err(ApiError::EmbeddingApiUnavailable("mock failure".into()))
    }

    fn dimension(&self) -> usize {
        128
    }

    fn model_name(&self) -> &str {
        "failing-embedding"
    }
}

struct MockTextGenerator {
    response: String,
}

impl TextGenerator for MockTextGenerator {
    fn generate(&self, _prompt: &str) -> Result<String, ApiError> {
        Ok(self.response.clone())
    }

    fn model_name(&self) -> &str {
        "mock-generator"
    }
}

struct FailingTextGenerator;

impl TextGenerator for FailingTextGenerator {
    fn generate(&self, _prompt: &str) -> Result<String, ApiError> {
        Err(ApiError::HyDeGenerationFailed(
            "mock generation failure".into(),
        ))
    }

    fn model_name(&self) -> &str {
        "failing-generator"
    }
}

// ── embed_fields ───────────────────────────────────────────────────────

#[test]
fn embed_fields_returns_three_embeddings() {
    let provider = MockEmbeddingProvider { dimension: 4 };
    let mut embedder = Embedder::new();

    let result = embedder
        .embed_fields(
            "user opened the editor and started typing a document",
            "typed a paragraph about rust programming language features",
            "the document was saved successfully to the local filesystem",
            &provider,
            None,
        )
        .unwrap();

    assert_eq!(result.context.len(), 4);
    assert_eq!(result.action.len(), 4);
    assert_eq!(result.result.len(), 4);
}

#[test]
fn embed_fields_caches_identical_text() {
    let provider = MockEmbeddingProvider { dimension: 4 };
    let mut embedder = Embedder::new();

    let same_text = "user opened the editor and started typing a long document about rust";

    embedder
        .embed_fields(same_text, same_text, same_text, &provider, None)
        .unwrap();

    assert_eq!(embedder.cache().len(), 1);
}

#[test]
fn embed_fields_different_texts_produce_different_embeddings() {
    let provider = MockEmbeddingProvider { dimension: 8 };
    let mut embedder = Embedder::new();

    let result = embedder
        .embed_fields(
            "user opened the editor window and began writing a new document from scratch",
            "typed several paragraphs about the rust programming language and its features",
            "the document was saved successfully to the local filesystem without any errors",
            &provider,
            None,
        )
        .unwrap();

    assert_ne!(result.context, result.action);
    assert_ne!(result.action, result.result);
    assert_ne!(result.context, result.result);
}

