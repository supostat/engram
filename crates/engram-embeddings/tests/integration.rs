// ── HyDE ───────────────────────────────────────────────────────────────

#[test]
fn hyde_triggered_for_short_text() {
    let provider = MockEmbeddingProvider { dimension: 4 };
    let generator = MockTextGenerator {
        response:
            "this is a hypothetical document generated by the text generator for short queries"
                .into(),
    };
    let mut embedder = Embedder::new();

    let short_query = "rust memory safety";

    embedder
        .embed_query(short_query, &provider, Some(&generator))
        .unwrap();

    // Cache should contain the hypothesis, not the raw query
    assert!(embedder.cache().get(short_query).is_none());
    assert!(embedder.cache().get(&generator.response).is_some());
}

#[test]
fn hyde_not_triggered_for_long_text() {
    let provider = MockEmbeddingProvider { dimension: 4 };
    let generator = MockTextGenerator {
        response:
            "hypothesis that should not be used because the input text is already long enough"
                .into(),
    };
    let mut embedder = Embedder::new();

    let long_text = "one two three four five six seven eight nine ten \
                     eleven twelve thirteen fourteen fifteen sixteen \
                     seventeen eighteen nineteen twenty";

    embedder
        .embed_query(long_text, &provider, Some(&generator))
        .unwrap();

    // Cache should contain the raw text, not the hypothesis
    assert!(embedder.cache().get(long_text).is_some());
    assert!(embedder.cache().get(&generator.response).is_none());
}

