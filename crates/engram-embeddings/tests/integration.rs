// ── embed_fields with HyDE ───────────────────────────────────────────

#[test]
fn embed_fields_with_hyde_uses_hypothesis_for_short_text() {
    let provider = MockEmbeddingProvider { dimension: 4 };
    let generator = MockTextGenerator {
        response:
            "this is a hypothetical document generated by the text generator for short fields"
                .into(),
    };
    let mut embedder = Embedder::new();

    let short_context = "rust memory";
    let short_action = "open editor";
    let short_result = "saved file";

    let result = embedder.embed_fields(
        short_context,
        short_action,
        short_result,
        &provider,
        Some(&generator),
    );

    assert!(result.is_ok());
    // All short fields should trigger HyDE and resolve to the same hypothesis
    assert!(embedder.cache().get(&generator.response).is_some());
    assert!(embedder.cache().get(short_context).is_none());
    assert!(embedder.cache().get(short_action).is_none());
    assert!(embedder.cache().get(short_result).is_none());
}
